class TestSmoothing:
    """Verify smooth_local_estimates matches R."""

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)
        self.mapping = _r_to_py_index_map(self.grid)

    def test_smoothing_a_and_b(self):
        """Test that smoothed a and b values match R (non-angular averaging)."""
        from weatherisk.estimation import smooth_local_estimates
        ref_raw = pd.read_csv(REF / "local_estimates_all.csv")
        ref_smooth = pd.read_csv(REF / "local_estimates_smoothed.csv")
        n = self.grid.n_grid
        mapping = self.mapping

        # Map raw estimates from R to Python order
        py_raw = np.zeros((n, 3))
        for r_idx in range(n):
            py_idx = mapping[r_idx]
            py_raw[py_idx, 0] = ref_raw.iloc[r_idx]["a_est"]
            py_raw[py_idx, 1] = ref_raw.iloc[r_idx]["b_est"]
            py_raw[py_idx, 2] = ref_raw.iloc[r_idx]["g_est"]

        # Read smoothing_dist from R scalar parameters
        scalars = pd.read_csv(REF / "scalar_parameters.csv")
        smoothing_dist = int(scalars.loc[scalars["parameter"] == "smoothing_dist", "value"].iloc[0])
        py_smooth = smooth_local_estimates(py_raw, smoothing_dist=smoothing_dist, grid=self.grid)

        # Map expected smoothed values from R to Python order
        expected = np.zeros((n, 3))
        for r_idx in range(n):
            py_idx = mapping[r_idx]
            expected[py_idx, 0] = ref_smooth.iloc[r_idx]["a_sm"]
            expected[py_idx, 1] = ref_smooth.iloc[r_idx]["b_sm"]
            expected[py_idx, 2] = ref_smooth.iloc[r_idx]["g_sm"]

        # a and b should match closely (simple arithmetic mean)
        np.testing.assert_allclose(py_smooth[:, 0], expected[:, 0], atol=1e-8,
                                   err_msg="Smoothed 'a' mismatch")
        np.testing.assert_allclose(py_smooth[:, 1], expected[:, 1], atol=1e-8,
                                   err_msg="Smoothed 'b' mismatch")

    def test_smoothing_gamma(self):
        """Test smoothed gamma (angle wrapping)."""
        from weatherisk.estimation import smooth_local_estimates
        ref_raw = pd.read_csv(REF / "local_estimates_all.csv")
        ref_smooth = pd.read_csv(REF / "local_estimates_smoothed.csv")
        n = self.grid.n_grid
        mapping = self.mapping

        py_raw = np.zeros((n, 3))
        for r_idx in range(n):
            py_idx = mapping[r_idx]
            py_raw[py_idx, 0] = ref_raw.iloc[r_idx]["a_est"]
            py_raw[py_idx, 1] = ref_raw.iloc[r_idx]["b_est"]
            py_raw[py_idx, 2] = ref_raw.iloc[r_idx]["g_est"]

        # Read smoothing_dist from R scalar parameters
        scalars = pd.read_csv(REF / "scalar_parameters.csv")
        smoothing_dist = int(scalars.loc[scalars["parameter"] == "smoothing_dist", "value"].iloc[0])
        py_smooth = smooth_local_estimates(py_raw, smoothing_dist=smoothing_dist, grid=self.grid)

        expected_g = np.zeros(n)
        for r_idx in range(n):
            py_idx = mapping[r_idx]
            expected_g[py_idx] = ref_smooth.iloc[r_idx]["g_sm"]

        # Gamma uses circular averaging, allow slightly more tolerance
        np.testing.assert_allclose(py_smooth[:, 2], expected_g, atol=1e-6,
                                   err_msg="Smoothed 'gamma' mismatch")


