    def test_lec_cluster_assignments_equivalent(self):
        """LEC cluster assignments produce the same partition as R.

        Cluster label numbers may differ, but the PARTITION must be identical:
        two grid points in the same cluster in R must be in the same cluster
        in Python, and vice versa.
        """
        from weatherisk.clustering import clustering
        from scipy.cluster.hierarchy import fcluster

        ref_params = pd.read_csv(REF / "lec_clustering_params.csv")
        k_r = int(ref_params["k_lec"].iloc[0])
