# ═══════════════════════════════════════════════════════════════════════════

class TestPipelineIntegration:
    """End-to-end test: same simulation data → same clusters."""

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)
        self.mapping = _r_to_py_index_map(self.grid)

    def test_madogram_from_simulation_to_clusters(self):
        """Full EDC pipeline: simulation → madogram → clustering → partition."""
        from weatherisk.clustering import (
            c_extrcoeff_matrix, clustering, cluster_number_threshold_method,
        )
        from scipy.cluster.hierarchy import fcluster

        # Load R simulation and reshape
        sim_ref = pd.read_csv(REF / "simulation_nonstat_seed42.csv")
        sim_cols = [c for c in sim_ref.columns if c.startswith("sim")]
        n_sim = len(sim_cols)
        n = self.grid.n_grid

        sim_3d = np.zeros((self.grid.nrow, self.grid.ncol, n_sim))
        for r_idx in range(n):
            r_i = r_idx % self.grid.nrow
            r_j = r_idx // self.grid.nrow
            for k, col in enumerate(sim_cols):
                sim_3d[r_i, r_j, k] = sim_ref.iloc[r_idx][col]

        # Step 1: Compute madogram
        py_mado = c_extrcoeff_matrix(sim_3d, madogram=True)

        # Step 2: Re-index to R order for threshold computation
        ref_mado = pd.read_csv(REF / "madogram_matrix.csv").values
        ref_reindexed = np.zeros_like(ref_mado)
        mapping = self.mapping
        for r_i in range(n):
            for r_j in range(n):
                ref_reindexed[mapping[r_i], mapping[r_j]] = ref_mado[r_i, r_j]

        np.testing.assert_allclose(py_mado, ref_reindexed, atol=1e-12)

        # Step 3: Cluster
        ref_params = pd.read_csv(REF / "saunders_clustering_params.csv")
        k_r = int(ref_params["k_saunders"].iloc[0])

        # Use Python's madogram directly for clustering
        hc = clustering(py_mado, method="average")
        q30 = np.quantile(py_mado[np.triu_indices(n, k=1)], 0.30)
        k_py = cluster_number_threshold_method(hc, q30)

        # UPGMA tie-breaking can differ → allow ±1 cluster
        assert abs(k_py - k_r) <= 1, f"k mismatch: Python={k_py}, R={k_r} (>1 apart)"
