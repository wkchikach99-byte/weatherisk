"""Cross-validation tests: verify Python weatherisk matches R reference outputs.

Every test loads reference CSV data generated by the R pipeline
(tests/generate_r_reference.R) and asserts that the Python implementation
produces numerically equivalent results.

The R code uses COLUMN-MAJOR (Fortran) ordering for flat indices;
Python uses ROW-MAJOR (C) ordering.  All comparisons are done by
matching on (X, Y) coordinates, not on flat indices.
"""

from __future__ import annotations

import os
from pathlib import Path

import numpy as np
import pandas as pd
import pytest
from scipy.cluster.hierarchy import fcluster
from scipy.spatial.distance import squareform

REF = Path(__file__).parent / "reference_data"


def _skip_if_no_ref():
    """Skip if reference data has not been generated."""
    if not (REF / "grid_coordinates.csv").exists():
        pytest.skip("R reference data not generated — run: Rscript tests/generate_r_reference.R")


# ---------------------------------------------------------------------------
#  Helper: build an R-index → Python-index mapping via coordinates
# ---------------------------------------------------------------------------

def _r_to_py_index_map(grid):
    """Return array m where m[r_flat_0based] = py_flat_0based.

    R flat index n (1-based, column-major) corresponds to coordinate
    (X_R[n], Y_R[n]).  Python flat index p (0-based, row-major) satisfies
    grid.X.flat[p] == X_R[n] and grid.Y.flat[p] == Y_R[n].
    """
    ref = pd.read_csv(REF / "grid_coordinates.csv")
    mapping = np.empty(len(ref), dtype=int)
    for idx, row in ref.iterrows():
        r_x, r_y = row["X"], row["Y"]
        py_idx = grid.koord_num(r_x, r_y)
        mapping[idx] = py_idx
    return mapping


