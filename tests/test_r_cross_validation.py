class TestGridCoordinates:
    """Verify grid construction matches R parameters.R."""

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)

    def test_axes(self):
        ref_x = pd.read_csv(REF / "x_axis.csv")["x_ax"].values
        ref_y = pd.read_csv(REF / "y_axis.csv")["y_ax"].values
        np.testing.assert_allclose(self.grid.x_ax, ref_x, atol=1e-12)
        np.testing.assert_allclose(self.grid.y_ax, ref_y, atol=1e-12)

    def test_grid_coordinates_match_r(self):
        """Every R coordinate pair exists in the Python grid."""
        ref = pd.read_csv(REF / "grid_coordinates.csv")
        for _, row in ref.iterrows():
            py_idx = self.grid.koord_num(row["X"], row["Y"])
            px, py = self.grid.number_koord(py_idx)
            assert abs(px - row["X"]) < 1e-10
            assert abs(py - row["Y"]) < 1e-10

    def test_dist_helpers_match_r(self):
        from weatherisk.grid import dist_x, dist_y

        ref = pd.read_csv(REF / "dist_helper_test_cases.csv")
        for _, row in ref.iterrows():
            np.testing.assert_allclose(dist_x(row["x1"], row["x2"]), row["dist_x"], atol=1e-14)
            np.testing.assert_allclose(dist_y(row["y1"], row["y2"]), row["dist_y"], atol=1e-14)

    def test_grid_number_matches_r(self):
        """grid_number / number_grid round-trip (accounting for 0/1-based)."""
        ref = pd.read_csv(REF / "grid_number_test_cases.csv")
        for _, row in ref.iterrows():
            r_i, r_j = int(row["i"]), int(row["j"])
            r_n = int(row["grid_num"])
            # R is 1-based, Python 0-based
            py_n = self.grid.grid_number(r_i - 1, r_j - 1)
            assert py_n == r_n - 1

    def test_number_grid_matches_r(self):
        ref = pd.read_csv(REF / "number_grid_test_cases.csv")
        for _, row in ref.iterrows():
            r_n = int(row["n"])
            r_i, r_j = int(row["i"]), int(row["j"])
            py_i, py_j = self.grid.number_grid(r_n - 1)
            assert py_i == r_i - 1
            assert py_j == r_j - 1

    def test_koord_num_same_coordinates(self):
        """koord_num returns a point with the same coordinates as R."""
        ref = pd.read_csv(REF / "koord_num_test_cases.csv")
        for _, row in ref.iterrows():
            py_idx = self.grid.koord_num(row["x"], row["y"])
            px, py = self.grid.number_koord(py_idx)
            assert abs(px - row["Xval"]) < 1e-10
            assert abs(py - row["Yval"]) < 1e-10

    def test_parameter_matrices(self):
        """Stripes preset parameter matrices match R."""
        from weatherisk.parameters import get_preset
        preset = get_preset("stripes")
        a_py = preset.a_func(self.grid.X, self.grid.Y)
        b_py = preset.b_func(self.grid.X, self.grid.Y)
        g_py = preset.g_func(self.grid.X, self.grid.Y)

        ref = pd.read_csv(REF / "parameter_matrices.csv")
        # Compare by coordinate matching
        mapping = _r_to_py_index_map(self.grid)

        for r_idx in range(len(ref)):
            py_idx = mapping[r_idx]
            py_row, py_col = self.grid.number_grid(py_idx)
            assert abs(a_py[py_row, py_col] - ref.iloc[r_idx]["a"]) < 1e-10
            assert abs(b_py[py_row, py_col] - ref.iloc[r_idx]["b"]) < 1e-10, \
                f"b mismatch at R index {r_idx}: py={b_py[py_row, py_col]}, R={ref.iloc[r_idx]['b']}"
            assert abs(g_py[py_row, py_col] - ref.iloc[r_idx]["g"]) < 1e-10


