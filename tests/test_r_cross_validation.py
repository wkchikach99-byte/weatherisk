#  6. MADOGRAM / EXTREMAL COEFFICIENT MATRIX
# ═══════════════════════════════════════════════════════════════════════════

class TestMadogramMatrix:
    """Verify madogram and extremal coefficient matrices match R."""

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)
        self.mapping = _r_to_py_index_map(self.grid)
        # Load R simulation data and reshape to (10,10,10) in R order
        sim_ref = pd.read_csv(REF / "simulation_nonstat_seed42.csv")
        sim_cols = [c for c in sim_ref.columns if c.startswith("sim")]
        self.n_sim = len(sim_cols)
        # Build 3D array in R's column-major grid order
        # R: sim_all[i,j,k] where i=row, j=col of 10x10 grid
        # flat_index in R is column-major: (i,j) -> (j-1)*nrow + i
        n = self.grid.n_grid
        self.sim_data_r_order = np.zeros((n, self.n_sim))
        for k, col in enumerate(sim_cols):
            self.sim_data_r_order[:, k] = sim_ref[col].values

    def test_madogram_matrix(self):
        from weatherisk.clustering import c_extrcoeff_matrix
        ref_mado = pd.read_csv(REF / "madogram_matrix.csv").values
        n = self.grid.n_grid

        # Reshape simulation data to (nrow, ncol, n_sim) — must match R layout
        # R's flat_index is column-major, so sim_data_r_order[r_idx, k]
        # corresponds to grid point r_idx (R's column-major order).
        sim_3d = np.zeros((self.grid.nrow, self.grid.ncol, self.n_sim))
        for r_idx in range(n):
            # Convert R flat index (0-based) to (row, col) in column-major order
            r_i = r_idx % self.grid.nrow
            r_j = r_idx // self.grid.nrow
            sim_3d[r_i, r_j, :] = self.sim_data_r_order[r_idx, :]

        py_mado = c_extrcoeff_matrix(sim_3d, madogram=True)

        # py_mado is indexed in Python's flat order; ref_mado in R's column-major.
        # We need to re-index. Both are n×n symmetric matrices indexed by
        # their respective flat orderings.
        # Map: py_mado[py_i, py_j] should equal ref_mado[r_i, r_j]
        #   where py_i = mapping[r_i], py_j = mapping[r_j]
        mapping = self.mapping
        ref_reindexed = np.zeros_like(ref_mado)
        for r_i in range(n):
            for r_j in range(n):
                ref_reindexed[mapping[r_i], mapping[r_j]] = ref_mado[r_i, r_j]

        np.testing.assert_allclose(py_mado, ref_reindexed, atol=1e-12,
                                   err_msg="Madogram matrix mismatch")

    def test_extremal_coefficient_matrix(self):
        from weatherisk.clustering import c_extrcoeff_matrix
        ref_ec = pd.read_csv(REF / "extremal_coefficient_matrix.csv").values
        n = self.grid.n_grid

        sim_3d = np.zeros((self.grid.nrow, self.grid.ncol, self.n_sim))
        for r_idx in range(n):
            r_i = r_idx % self.grid.nrow
            r_j = r_idx // self.grid.nrow
            sim_3d[r_i, r_j, :] = self.sim_data_r_order[r_idx, :]

        py_ec = c_extrcoeff_matrix(sim_3d, madogram=False)

        mapping = self.mapping
        ref_reindexed = np.zeros_like(ref_ec)
        for r_i in range(n):
            for r_j in range(n):
                ref_reindexed[mapping[r_i], mapping[r_j]] = ref_ec[r_i, r_j]

        np.testing.assert_allclose(py_ec, ref_reindexed, atol=1e-12,
                                   err_msg="Extremal coefficient matrix mismatch")


