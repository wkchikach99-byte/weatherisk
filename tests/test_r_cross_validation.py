class TestCovarianceMatrices:
    """Verify full n×n covariance matrices match R."""

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)
        self.mapping = _r_to_py_index_map(self.grid)

    def test_stationary_covariance_matrix(self):
        from weatherisk.covariance import cov_fkt_2d
        ref = pd.read_csv(REF / "covariance_matrix_stationary.csv").values
        n = self.grid.n_grid
        assert ref.shape == (n, n)

        # Build Python matrix using Fortran-order coordinates to match R
        X_f = self.grid.X.flatten('F')
        Y_f = self.grid.Y.flatten('F')
        py_cov = np.zeros((n, n))
        for i in range(n):
            for j in range(n):
                py_cov[i, j] = cov_fkt_2d(
                    X_f[i] - X_f[j], Y_f[i] - Y_f[j],
                    alpha=1.0, a=2.0, b=1.0, g=0.0
                )
        np.testing.assert_allclose(py_cov, ref, atol=1e-12)

    def test_nonstationary_covariance_matrix(self):
        from weatherisk.covariance import cov_fkt_2d_nonstat2
        from weatherisk.parameters import get_preset
        ref = pd.read_csv(REF / "covariance_matrix_nonstationary.csv").values
        preset = get_preset("stripes")
        n = self.grid.n_grid

        a_mat = preset.a_func(self.grid.X, self.grid.Y)
        b_mat = preset.b_func(self.grid.X, self.grid.Y)
        g_mat = preset.g_func(self.grid.X, self.grid.Y)

        # Flatten in Fortran order to match R
        X_f = self.grid.X.flatten('F')
        Y_f = self.grid.Y.flatten('F')
        a_f = a_mat.flatten('F')
        b_f = b_mat.flatten('F')
        g_f = g_mat.flatten('F')

        py_cov = np.zeros((n, n))
        for i in range(n):
            for j in range(n):
                py_cov[i, j] = cov_fkt_2d_nonstat2(
                    X_f[i] - X_f[j], Y_f[i] - Y_f[j], alpha=1.0,
                    a1=a_f[i], b1=b_f[i], g1=g_f[i],
                    a2=a_f[j], b2=b_f[j], g2=g_f[j],
                )
        np.testing.assert_allclose(py_cov, ref, atol=1e-10)


