class TestLogLikelihoodInClusters:
    """Verify llh_in_cluster matches R for given parameters and data."""

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)
        self.mapping = _r_to_py_index_map(self.grid)

    def test_llh_in_cluster_values(self):
        """Log-likelihood per LEC cluster matches R."""
        from weatherisk.estimation import llh_in_cluster

        ref_llh = pd.read_csv(REF / "llh_in_clusters_lec.csv")
        ref_clusters = pd.read_csv(REF / "clusters_lec.csv")

        # Load simulation data in R order
        sim_ref = pd.read_csv(REF / "simulation_nonstat_seed42.csv")
        sim_cols = [c for c in sim_ref.columns if c.startswith("sim")]
        n_sim = len(sim_cols)
        n = self.grid.n_grid

        sim_data = np.zeros((n, n_sim))
        for k, col in enumerate(sim_cols):
            sim_data[:, k] = sim_ref[col].values

        # Reshape to 3D: [nrow, ncol, n_sim] in R's row/col order
        sim_3d = np.zeros((self.grid.nrow, self.grid.ncol, n_sim))
        for r_idx in range(n):
            r_i = r_idx % self.grid.nrow
            r_j = r_idx // self.grid.nrow
            sim_3d[r_i, r_j, :] = sim_data[r_idx, :]

        r_clusters = ref_clusters["cluster"].values  # R column-major order
        resolution = self.grid.resolution
        max_dist = 4 * ((self.grid.x_ax[-1] - self.grid.x_ax[0]) / (resolution - 1))

        for _, row in ref_llh.iterrows():
            cl = int(row["cluster"])
            locest = np.array([row["a"], row["b"], row["g"]])

            # Get R indices for this cluster (0-based)
            which_cl_r = np.where(r_clusters == cl)[0]

            # Convert R flat indices to (i, j) in R's column-major order
            r_rows = which_cl_r % self.grid.nrow
            r_cols = which_cl_r // self.grid.nrow

            # Extract X, Y coordinates (R order)
            X_cl = self.grid.X.flatten('F')[which_cl_r]
            Y_cl = self.grid.Y.flatten('F')[which_cl_r]

            # Build z matrix: [n_points_in_cluster, n_sim]
            z_cl = np.zeros((len(which_cl_r), n_sim))
            for k, r_idx in enumerate(which_cl_r):
                r_i = r_idx % self.grid.nrow
                r_j = r_idx // self.grid.nrow
                z_cl[k, :] = sim_3d[r_i, r_j, :]

            py_llh = llh_in_cluster(
                z_cl, df=5.0, alpha=1.0,
                X=X_cl, Y=Y_cl,
                locest=locest,
                max_dist=max_dist,
                average=True,
            )

            np.testing.assert_allclose(
                py_llh, row["llh"], atol=1e-4, rtol=1e-4,
                err_msg=f"LLH cluster {cl}: Python={py_llh}, R={row['llh']}"
            )


