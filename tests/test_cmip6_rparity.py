# ═══════════════════════════════════════════════════════════════════════════
#  4. Detrending: _detrend_grid_fast must use STL, not running mean
# ═══════════════════════════════════════════════════════════════════════════

class TestDetrending:
    """Contzen et al. (2025) specifies STL detrending (Cleveland et al. 1990).
    The current _detrend_grid_fast uses a cumsum-based running mean.
    """

    def test_detrend_output_matches_stl_not_running_mean(self):
        """_detrend_grid_fast output must be closer to STL than to a
        running mean on synthetic monthly precipitation data."""
        pytest.importorskip("statsmodels")
        from statsmodels.tsa.seasonal import STL
        from weatherisk.cmip6_pipeline import _detrend_grid_fast

        rng = np.random.default_rng(42)
        n_years, n_months = 50, 600
        t = np.arange(n_months, dtype=float)

        seasonal = 5.0 * np.sin(2 * np.pi * t / 12)
        trend = 0.001 * t + 0.00001 * t ** 2
        noise = rng.normal(0, 1.5, n_months)
        pr_1d = np.maximum(20.0 + seasonal + trend + noise, 0.1)

        # Pipeline
        result = _detrend_grid_fast(
            pr_1d.reshape(n_months, 1, 1), period=12, verbose=False,
        )
        pipe_det = result[:, 0, 0]

        # STL reference
        stl_det = pr_1d - STL(pr_1d, period=12, robust=True).fit().trend

        # Running-mean reference
        half = 60
        pad = np.concatenate([pr_1d[half - 1::-1], pr_1d, pr_1d[-1:-half - 1:-1]])
        cs = np.concatenate([[0], np.cumsum(pad)])
        rm_trend = (cs[121:] - cs[:-121]) / 121
        rm_det = pr_1d - rm_trend[: len(pr_1d)]

        dist_to_stl = np.max(np.abs(pipe_det - stl_det))
        dist_to_rm = np.max(np.abs(pipe_det - rm_det))

        assert dist_to_stl < dist_to_rm, (
            f"Pipeline output is closer to running mean "
            f"(dist_rm={dist_to_rm:.4f}) than to STL "
            f"(dist_stl={dist_to_stl:.4f}). Paper specifies STL."
        )


# ═══════════════════════════════════════════════════════════════════════════
#  5. Mini CMIP6 pipeline: end-to-end R comparison
# ═══════════════════════════════════════════════════════════════════════════

class TestMiniCMIP6Pipeline:
    """End-to-end comparison against R reference outputs generated by
    tests/generate_cmip6_mini_reference.R.

    Workflow: monthly precip → STL detrend → annual maxima → GEV →
    Fréchet → EDC madogram → clustering → k.
    LEC path: Fréchet → local MLE → smoothing → ellipse dissimilarity → k.

    Each test replays one step using the CMIP6 pipeline's Python code
    and compares against the R intermediate output.

    Skipped if the R reference data has not been generated.
    """

    def setup_method(self):
        _skip_if_no_mini_ref()

    def test_stl_detrending_matches_r(self):
        """_detrend_grid_fast output must match R's STL detrended data."""
        from weatherisk.cmip6_pipeline import _detrend_grid_fast

        # Load R's input and output
        params = pd.read_csv(CMIP6_MINI_REF / "scalar_params.csv")
        n_months = int(params.loc[params["parameter"] == "n_months", "value"].iloc[0])
        n_grid = int(params.loc[params["parameter"] == "n_grid", "value"].iloc[0])
        nrow = int(params.loc[params["parameter"] == "nrow_grid", "value"].iloc[0])
        ncol = int(params.loc[params["parameter"] == "ncol_grid", "value"].iloc[0])

        pr_flat = pd.read_csv(CMIP6_MINI_REF / "monthly_pr.csv").values
        r_det_flat = pd.read_csv(CMIP6_MINI_REF / "detrended.csv").values

        # Reshape from R column-major to (n_months, nrow, ncol)
        pr_3d = np.zeros((n_months, nrow, ncol))
        r_det_3d = np.zeros((n_months, nrow, ncol))
        for r_idx in range(n_grid):
            r_i = r_idx % nrow
            r_j = r_idx // nrow
            pr_3d[:, r_i, r_j] = pr_flat[:, r_idx]
            r_det_3d[:, r_i, r_j] = r_det_flat[:, r_idx]

        # Run Python pipeline detrending
        py_det = _detrend_grid_fast(pr_3d, period=12, verbose=False)

        # Compare — should match R's STL output
        max_diff = np.max(np.abs(py_det - r_det_3d))
        np.testing.assert_allclose(
            py_det, r_det_3d, atol=0.5,
            err_msg=(
                f"_detrend_grid_fast differs from R's STL. "
                f"Max absolute diff = {max_diff:.4f}. "
                f"Pipeline uses running mean; R uses STL."
            ),
        )

    def test_edc_madogram_matches_r(self):
        """EDC madogram from pipeline functions must match R's madogram."""
        from weatherisk.cmip6_pipeline import _edc_matrix_flat

        params = pd.read_csv(CMIP6_MINI_REF / "scalar_params.csv")
        n_years = int(params.loc[params["parameter"] == "n_years", "value"].iloc[0])
        n_grid = int(params.loc[params["parameter"] == "n_grid", "value"].iloc[0])
        nrow = int(params.loc[params["parameter"] == "nrow_grid", "value"].iloc[0])

        frechet_flat = pd.read_csv(CMIP6_MINI_REF / "frechet.csv").values
        r_mado = pd.read_csv(CMIP6_MINI_REF / "madogram_matrix.csv").values

        # _edc_matrix_flat takes (n_years, n_cells) — feed in R column-major order
        edc_pipe = _edc_matrix_flat(frechet_flat)

        np.testing.assert_allclose(
            edc_pipe, r_mado, atol=1e-10,
            err_msg=(
                f"Pipeline EDC matrix differs from R's madogram. "
                f"Pipeline max={edc_pipe.max():.4f}, "
                f"R max={r_mado[np.triu_indices_from(r_mado, k=1)].max():.4f}. "
                f"Pipeline applies EC-1 transform; R returns raw madogram."
            ),
        )

    def test_edc_k_matches_r(self):
        """EDC cluster count k from pipeline must match R's k_EDC."""
        from weatherisk.clustering import clustering, cluster_number_threshold_method
        from weatherisk.cmip6_pipeline import _edc_condensed_flat

        params = pd.read_csv(CMIP6_MINI_REF / "scalar_params.csv")
        r_edc_params = pd.read_csv(CMIP6_MINI_REF / "edc_clustering_params.csv")
        k_r = int(r_edc_params["k_edc"].iloc[0])

        frechet_flat = pd.read_csv(CMIP6_MINI_REF / "frechet.csv").values
        edc = _edc_condensed_flat(frechet_flat)
        q30 = float(np.quantile(edc, 0.30))
        hc = clustering(edc)
        k_pipe = max(2, cluster_number_threshold_method(hc, q30))

        assert k_pipe == k_r, (
            f"Pipeline EDC k={k_pipe} ≠ R k_EDC={k_r}. "
            f"Pipeline q30={q30:.4f}."
        )

    def test_gev_frechet_matches_r(self):
        """Python's fit_gev + to_frechet must match R's GEV + Fréchet.

        Both implementations use MLE for GEV fitting (Python: scipy
        genextreme.fit; R: hand-coded NLL + Nelder-Mead).  The Fréchet
        transform is mathematically equivalent (CDF-based and direct
        formula give identical results given the same GEV params).

        Empirically the max relative Fréchet diff is ~0.1%, so rtol=0.01
        (1%) is generous.  This is an *irreducible* difference from the
        fitting algorithm, NOT a bug — it just needs to be small.
        """
        from weatherisk.extremes import fit_gev, to_frechet

        params = pd.read_csv(CMIP6_MINI_REF / "scalar_params.csv")
        n_grid = int(params.loc[params["parameter"] == "n_grid", "value"].iloc[0])

        am = pd.read_csv(CMIP6_MINI_REF / "annual_maxima.csv").values
        r_gev = pd.read_csv(CMIP6_MINI_REF / "gev_params.csv")
        r_frechet = pd.read_csv(CMIP6_MINI_REF / "frechet.csv").values

        py_frechet = np.zeros_like(r_frechet)
        py_gev = np.zeros((n_grid, 3))
        for s in range(n_grid):
            loc, scale, shape = fit_gev(am[:, s])
            py_gev[s] = [loc, scale, shape]
            py_frechet[:, s] = to_frechet(am[:, s], loc, scale, shape)

        r_gev_arr = np.column_stack([
            r_gev["loc"], r_gev["scale"], r_gev["shape"],
        ])

        # GEV params should agree within ~0.001 (irreducible fitting diff)
        np.testing.assert_allclose(
            py_gev, r_gev_arr, atol=0.01,
            err_msg="GEV params differ from R beyond fitting tolerance.",
        )

        # Fréchet values: max relative diff is ~0.1% empirically
        np.testing.assert_allclose(
            py_frechet, r_frechet, rtol=0.01,
            err_msg=(
                f"Fréchet differs from R. "
                f"Max abs diff={np.abs(py_frechet - r_frechet).max():.4f}."
            ),
        )

    @pytest.mark.parametrize("backend", _BACKENDS)
    def test_local_mle_matches_r(self, backend):
        """Local MLE estimates from pipeline must match R's estimates.

        Uses _local_mle_one_cmip6 (→ optimize_local_mle) for each cell.
        R uses pairwise_density_optim_local with parscale, retry, b≥0.01.
        Python's optimize_local_mle lacks these → estimates will differ.

        Parametrized over backend={python, rust}.
        """
        from weatherisk.cmip6_pipeline import CMIP6Config, _local_mle_one_cmip6

        grid_df = pd.read_csv(CMIP6_MINI_REF / "grid_coordinates.csv")
        frechet = pd.read_csv(CMIP6_MINI_REF / "frechet.csv").values
        r_est = pd.read_csv(CMIP6_MINI_REF / "local_estimates_all.csv")
        lec_params = pd.read_csv(CMIP6_MINI_REF / "lec_scalar_params.csv")
        abstand = int(
            lec_params.loc[lec_params["parameter"] == "locest_abst", "value"].iloc[0]
        )

        grid_coords = np.column_stack([
            grid_df["Y"].values, grid_df["X"].values,
        ])

        cfg = CMIP6Config(
            neighbor_radius=float(abstand),
            smoothing_radius=2.0,
            df=5.0,
            alpha=1.0,
        )

        n_grid = len(grid_df)
        py_est = np.zeros((n_grid, 3))
        with _force_backend(backend):
            for cidx in range(n_grid):
                py_est[cidx] = _local_mle_one_cmip6(
                    frechet, cidx, grid_coords, cfg,
                )

        r_est_arr = np.column_stack([
            r_est["a_est"].values, r_est["b_est"].values, r_est["g_est"].values,
        ])

        ab_diff = np.abs(py_est[:, :2] - r_est_arr[:, :2])
        max_a_diff = ab_diff[:, 0].max()
        max_b_diff = ab_diff[:, 1].max()
        n_mismatch = np.sum(np.any(ab_diff > 1.0, axis=1))

        worst_idx = np.argmax(ab_diff[:, 1])
        np.testing.assert_allclose(
            py_est[:, :2], r_est_arr[:, :2], atol=1.0,
            err_msg=(
                f"Local MLE [{backend}] (a,b) differ from R "
                f"at {n_mismatch}/{n_grid} cells. "
                f"Max a diff={max_a_diff:.3f}, max b diff={max_b_diff:.3f}. "
                f"Worst cell {worst_idx}: "
                f"py=({py_est[worst_idx, 0]:.3f}, {py_est[worst_idx, 1]:.3f}) "
                f"R=({r_est_arr[worst_idx, 0]:.3f}, {r_est_arr[worst_idx, 1]:.3f}). "
                f"optimize_local_mle lacks parscale/retry and has b_lower=0.0."
            ),
        )

