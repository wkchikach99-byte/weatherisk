# ═══════════════════════════════════════════════════════════════════════════
#  4. Detrending: _detrend_grid_fast must use STL, not running mean
# ═══════════════════════════════════════════════════════════════════════════

class TestDetrending:
    """Contzen et al. (2025) specifies STL detrending (Cleveland et al. 1990).
    The current _detrend_grid_fast uses a cumsum-based running mean.
    """

    def test_detrend_output_matches_stl_not_running_mean(self):
        """_detrend_grid_fast output must be closer to STL than to a
        running mean on synthetic monthly precipitation data."""
        pytest.importorskip("statsmodels")
        from statsmodels.tsa.seasonal import STL
        from weatherisk.cmip6_pipeline import _detrend_grid_fast

        rng = np.random.default_rng(42)
        n_years, n_months = 50, 600
        t = np.arange(n_months, dtype=float)

        seasonal = 5.0 * np.sin(2 * np.pi * t / 12)
        trend = 0.001 * t + 0.00001 * t ** 2
        noise = rng.normal(0, 1.5, n_months)
        pr_1d = np.maximum(20.0 + seasonal + trend + noise, 0.1)

        # Pipeline
        result = _detrend_grid_fast(
            pr_1d.reshape(n_months, 1, 1), period=12, verbose=False,
        )
        pipe_det = result[:, 0, 0]

        # STL reference
        stl_det = pr_1d - STL(pr_1d, period=12, robust=True).fit().trend

        # Running-mean reference
        half = 60
        pad = np.concatenate([pr_1d[half - 1::-1], pr_1d, pr_1d[-1:-half - 1:-1]])
        cs = np.concatenate([[0], np.cumsum(pad)])
        rm_trend = (cs[121:] - cs[:-121]) / 121
        rm_det = pr_1d - rm_trend[: len(pr_1d)]

        dist_to_stl = np.max(np.abs(pipe_det - stl_det))
        dist_to_rm = np.max(np.abs(pipe_det - rm_det))

        assert dist_to_stl < dist_to_rm, (
            f"Pipeline output is closer to running mean "
            f"(dist_rm={dist_to_rm:.4f}) than to STL "
            f"(dist_stl={dist_to_stl:.4f}). Paper specifies STL."
        )

    def test_mini_detrending_matches_r_tightly(self):
        """Mini CMIP6 detrending should stay close to R's STL output."""
        from weatherisk.cmip6_pipeline import _detrend_grid_fast

        mini = _load_cmip6_mini_inputs()
        r_det_flat = pd.read_csv(CMIP6_MINI_REF / "detrended.csv").values
        n_months, nrow, ncol = mini["pr_3d"].shape
        n_grid = nrow * ncol

        r_det_3d = np.zeros((n_months, nrow, ncol))
        for r_idx in range(n_grid):
            r_i = r_idx % nrow
            r_j = r_idx // nrow
            r_det_3d[:, r_i, r_j] = r_det_flat[:, r_idx]

        py_det = _detrend_grid_fast(mini["pr_3d"], period=12, n_workers=1, verbose=False)
        abs_diff = np.abs(py_det - r_det_3d)
        max_diff = float(abs_diff.max())
        mean_diff = float(abs_diff.mean())

        assert max_diff < 0.12, (
            f"Mini CMIP6 detrending drifted too far from R STL: max abs diff={max_diff:.6f}."
        )
        assert mean_diff < 0.01, (
            f"Mini CMIP6 detrending mean abs diff={mean_diff:.6f} is too large."
        )


