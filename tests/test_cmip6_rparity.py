REF = Path(__file__).parent / "reference_data"
CMIP6_MINI_REF = REF / "cmip6_mini"

# Backend IDs used by parametrized tests.
_BACKENDS = ["python"]


@contextlib.contextmanager
def _force_backend(name: str):
    """Temporarily force weatherisk.backend to use a specific backend.

    Patches the module-level ``_USE_RUST`` flag so that
    ``_nll_and_grad_factory`` / ``calc_distance_ellipses`` dispatch
    to the requested engine for the duration of the context.
    """
    import weatherisk.backend as _be
    old = _be._USE_RUST
    _be._USE_RUST = False
    try:
        yield
    finally:
        _be._USE_RUST = old


def _skip_if_no_ref():
    """Skip if main R reference data has not been generated."""
    if not (REF / "grid_coordinates.csv").exists():
        pytest.skip(
            "R reference data not generated — run: "
            "Rscript tests/generate_r_reference.R"
        )


def _skip_if_no_mini_ref():
    """Skip if mini CMIP6 R reference data has not been generated."""
    if not (CMIP6_MINI_REF / "detrended.csv").exists():
        pytest.skip(
            "Mini CMIP6 reference data not generated — run: "
            "Rscript tests/generate_cmip6_mini_reference.R"
        )


def _load_cmip6_mini_inputs():
    """Load mini CMIP6 monthly input and metadata from R reference files."""
    _skip_if_no_mini_ref()

    params = pd.read_csv(CMIP6_MINI_REF / "scalar_params.csv")
    n_months = int(params.loc[params["parameter"] == "n_months", "value"].iloc[0])
    n_grid = int(params.loc[params["parameter"] == "n_grid", "value"].iloc[0])
    nrow = int(params.loc[params["parameter"] == "nrow_grid", "value"].iloc[0])
    ncol = int(params.loc[params["parameter"] == "ncol_grid", "value"].iloc[0])

    pr_flat = pd.read_csv(CMIP6_MINI_REF / "monthly_pr.csv").values
    pr_3d = np.zeros((n_months, nrow, ncol))
    for r_idx in range(n_grid):
        r_i = r_idx % nrow
        r_j = r_idx // nrow
        pr_3d[:, r_i, r_j] = pr_flat[:, r_idx]

    times = pd.date_range("2000-01-01", periods=n_months, freq="MS").to_numpy()
    grid_df = pd.read_csv(CMIP6_MINI_REF / "grid_coordinates.csv")
    grid_coords = np.column_stack([grid_df["Y"].values, grid_df["X"].values])
    lec_params = pd.read_csv(CMIP6_MINI_REF / "lec_scalar_params.csv")

    return dict(
        params=params,
        pr_3d=pr_3d,
        times=times,
        grid_coords=grid_coords,
        grid_df=grid_df,
        lec_params=lec_params,
    )


def _run_cmip6_mini_pipeline(backend: str = "python"):
    """Run the mini CMIP6 pipeline end-to-end on the R reference input."""
    from weatherisk.cmip6_pipeline import (
        CMIP6Config,
        _detrend_grid_fast,
        _monthly_annual_maxima,
        _run_clustering_cmip6,
        _run_local_estimation_cmip6,
        _smooth_estimates_cmip6,
    )
    from weatherisk.extremes import fit_gev, to_frechet

    mini = _load_cmip6_mini_inputs()
    lec_params = mini["lec_params"]
    cfg = CMIP6Config(
        neighbor_radius=float(
            lec_params.loc[lec_params["parameter"] == "locest_abst", "value"].iloc[0]
        ),
        smoothing_radius=float(
            lec_params.loc[lec_params["parameter"] == "smoothing_dist", "value"].iloc[0]
        ),
        df=5.0,
        alpha=1.0,
        mle_ensemble=int(
            lec_params.loc[lec_params["parameter"] == "locest_ensemble", "value"].iloc[0]
        ),
        quantile_threshold=0.30,
        n_workers=1,
        retain_clustering_artifacts=True,
    )

    with _force_backend(backend):
        pr_det = _detrend_grid_fast(mini["pr_3d"], period=12, n_workers=1, verbose=False)
        annual_max, years = _monthly_annual_maxima(pr_det, mini["times"], verbose=False)
        annual_max_flat = annual_max.reshape(annual_max.shape[0], -1, order="F")

        frechet = np.zeros_like(annual_max_flat)
        gev = np.zeros((annual_max_flat.shape[1], 3))
        for s in range(annual_max_flat.shape[1]):
            loc, scale, shape = fit_gev(annual_max_flat[:, s])
            gev[s] = [loc, scale, shape]
            frechet[:, s] = to_frechet(annual_max_flat[:, s], loc, scale, shape)

        raw = _run_local_estimation_cmip6(frechet, mini["grid_coords"], cfg, verbose=False)
        smoothed = _smooth_estimates_cmip6(raw, mini["grid_coords"], cfg, verbose=False)
        clustering_out = _run_clustering_cmip6(smoothed, frechet, cfg, verbose=False)

    return dict(
        mini=mini,
        cfg=cfg,
        detrended=pr_det,
        annual_max=annual_max_flat,
        years=years,
        gev=gev,
        frechet=frechet,
        raw=raw,
        smoothed=smoothed,
        clustering=clustering_out,
    )


def _load_r_simulation(grid):
    """Load R simulation data and reshape to (nrow, ncol, n_sim)."""
    sim_ref = pd.read_csv(REF / "simulation_nonstat_seed42.csv")
    sim_cols = [c for c in sim_ref.columns if c.startswith("sim")]
    n_sim = len(sim_cols)

    sim_3d = np.zeros((grid.nrow, grid.ncol, n_sim))
    for r_idx in range(grid.n_grid):
        r_i = r_idx % grid.nrow   # column-major: row = idx % nrow
        r_j = r_idx // grid.nrow  # col = idx // nrow
        for k, col in enumerate(sim_cols):
            sim_3d[r_i, r_j, k] = sim_ref.iloc[r_idx][col]

    return sim_3d, n_sim


def _build_pair_arrays(sim_3d, grid, xy_pos, abstand):
    """Build (zi, zj, xl, yl) pair arrays for a grid point.

    Replicates the pair construction in pairwise_density_optim_local
    so we can feed the same inputs to optimize_local_mle.
    """
    i0, j0 = grid.number_grid(xy_pos)
    n_sim = sim_3d.shape[2]

    sel_indices = []
    for dx in range(-abstand, abstand + 1):
        for dy in range(-abstand, abstand + 1):
            if 0 < dx * dx + dy * dy <= abstand * abstand:
                ni, nj = i0 + dy, j0 + dx
                if 0 <= ni < grid.nrow and 0 <= nj < grid.ncol:
                    sel_indices.append(grid.grid_number(ni, nj))

    if not sel_indices:
        return None, None, None, None

    sel_indices = np.array(sel_indices)
    ci, cj = grid.number_grid(xy_pos)
    z_centre = sim_3d[ci, cj, :]

    sel_rows = np.array([grid.number_grid(idx) for idx in sel_indices])
    z_neighbours = sim_3d[sel_rows[:, 0], sel_rows[:, 1], :]

    zi = z_neighbours.reshape(-1)
    zj = np.tile(z_centre, len(sel_indices))
    xl = np.repeat(grid.X_flat[sel_indices] - grid.X_flat[xy_pos], n_sim)
    yl = np.repeat(grid.Y_flat[sel_indices] - grid.Y_flat[xy_pos], n_sim)

    return zi, zj, xl, yl


