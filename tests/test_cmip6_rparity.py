class TestEDCAgainstRValidated:
    """Compare CMIP6 pipeline EDC functions to the R-validated
    c_extrcoeff_matrix(madogram=True) on the R simulation data.

    c_extrcoeff_matrix is verified against R's madogram_matrix.csv
    in test_r_cross_validation.py::TestMadogramMatrix.

    Currently FAILS because _edc_matrix_flat / _edc_condensed_flat apply
    the EC-1 transform (values in [0, 1]) instead of returning raw
    madogram values (in [0, 0.5)).
    """

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)
        self.sim_3d, self.n_sim = _load_r_simulation(self.grid)

    def test_edc_matrix_flat_matches_validated_madogram(self):
        """_edc_matrix_flat must produce identical values to
        c_extrcoeff_matrix(madogram=True) on the same R simulation data."""
        from weatherisk.clustering import c_extrcoeff_matrix
        from weatherisk.cmip6_pipeline import _edc_matrix_flat

        # R-validated reference (known correct — matches R's madogram_matrix.csv)
        # c_extrcoeff_matrix flattens with order='F' (column-major = R order)
        mado_ref = c_extrcoeff_matrix(self.sim_3d, madogram=True)

        # CMIP6 pipeline: _edc_matrix_flat takes (n_years, n_cells)
        # Flatten sim_3d in column-major order to match internal ordering
        frechet_2d = self.sim_3d.reshape(
            self.grid.n_grid, self.n_sim, order='F'
        ).T  # → (n_sim, n_grid) with columns in column-major cell order

        edc_pipe = _edc_matrix_flat(frechet_2d)

        np.testing.assert_allclose(
            edc_pipe, mado_ref, atol=1e-12,
            err_msg=(
                f"_edc_matrix_flat does not match c_extrcoeff_matrix(madogram=True). "
                f"Pipeline max={edc_pipe.max():.4f}, "
                f"reference max={mado_ref[np.triu_indices_from(mado_ref, k=1)].max():.4f}. "
                f"The pipeline applies EC-1; it must return raw madogram."
            ),
        )

    def test_edc_condensed_flat_matches_validated_madogram(self):
        """Condensed (upper-triangle) form must also match the reference."""
        from weatherisk.clustering import c_extrcoeff_matrix
        from weatherisk.cmip6_pipeline import _edc_condensed_flat

        mado_ref = c_extrcoeff_matrix(self.sim_3d, madogram=True)
        ref_condensed = squareform(mado_ref)

        frechet_2d = self.sim_3d.reshape(
            self.grid.n_grid, self.n_sim, order='F'
        ).T
        edc_condensed = _edc_condensed_flat(frechet_2d)

        np.testing.assert_allclose(
            edc_condensed, ref_condensed, atol=1e-12,
            err_msg=(
                f"_edc_condensed_flat does not match condensed madogram. "
                f"Pipeline max={edc_condensed.max():.4f}, "
                f"reference max={ref_condensed.max():.4f}."
            ),
        )

    def test_edc_clustering_k_matches_r(self):
        """EDC clustering via pipeline functions must give the same k
        as R's saunders_clustering_params.csv (±1 for UPGMA tie-breaking)."""
        from weatherisk.clustering import (
            c_extrcoeff_matrix, clustering, cluster_number_threshold_method,
        )
        from weatherisk.cmip6_pipeline import _edc_condensed_flat

        # R reference k
        ref_params = pd.read_csv(REF / "saunders_clustering_params.csv")
        k_r = int(ref_params["k_saunders"].iloc[0])

        # Python reference path (R-validated)
        mado_ref = c_extrcoeff_matrix(self.sim_3d, madogram=True)
        mado_cond = squareform(mado_ref)
        q30_ref = float(np.quantile(mado_cond, 0.30))
        hc_ref = clustering(mado_cond)
        k_ref = cluster_number_threshold_method(hc_ref, q30_ref)

        # Pipeline path
        frechet_2d = self.sim_3d.reshape(
            self.grid.n_grid, self.n_sim, order='F'
        ).T
        edc_pipe = _edc_condensed_flat(frechet_2d)
        q30_pipe = float(np.quantile(edc_pipe, 0.30))
        hc_pipe = clustering(edc_pipe)
        k_pipe = cluster_number_threshold_method(hc_pipe, q30_pipe)

        assert k_pipe == k_ref, (
            f"Pipeline EDC k={k_pipe} (q30={q30_pipe:.4f}) ≠ "
            f"validated madogram k={k_ref} (q30={q30_ref:.4f}). "
            f"R k_saunders={k_r}. "
            f"Pipeline uses EC-1 scale; must use raw madogram."
        )


# ═══════════════════════════════════════════════════════════════════════════
#  2. MLE: optimize_local_mle must match R-validated pairwise_density_optim_local
# ═══════════════════════════════════════════════════════════════════════════

@pytest.mark.parametrize("backend", _BACKENDS)
class TestMLEAgainstRValidated:
    """Compare optimize_local_mle (CMIP6 pipeline) to
    pairwise_density_optim_local (R-validated) on the R simulation data.

    pairwise_density_optim_local has:
      - parscale = (ub - lb) / 100
      - boundary retry (up to 5 extra starts)
      - b lower bound = 0.01

    optimize_local_mle lacks all three.

    We build the same pair arrays for both functions from the R reference
    simulation data, then compare their (a, b, γ) estimates.

    Parametrized over backend={python, rust} to verify both gradient paths.
    """

    def setup_method(self):
        _skip_if_no_ref()
        from weatherisk.grid import Grid
        self.grid = Grid(resolution=10)
        self.sim_3d, self.n_sim = _load_r_simulation(self.grid)
        self.ref_pts = pd.read_csv(REF / "local_estimates_selected_points.csv")
        scalars = pd.read_csv(REF / "scalar_parameters.csv")
        self.abstand = int(
            scalars.loc[scalars["parameter"] == "locest_abst", "value"].iloc[0]
        )

    def test_optimize_local_mle_matches_validated_python(self, backend):
        """optimize_local_mle must produce estimates close to
        pairwise_density_optim_local at the R-reference selected points.

        pairwise_density_optim_local is already R-validated (it has parscale,
        boundary retry, b ≥ 0.01).  optimize_local_mle is the CMIP6 pipeline
        version that lacks these features.

        We call both with the same pair arrays and compare.  Any disagreement
        proves the algorithmic diffs matter.
        """
        from weatherisk.backend import optimize_local_mle
        from weatherisk.density import pairwise_density_optim_local

        mismatches = []
        for _, row in self.ref_pts.iterrows():
            x, y = row["X"], row["Y"]
            xy_pos = self.grid.koord_num(x, y)

            zi, zj, xl, yl = _build_pair_arrays(
                self.sim_3d, self.grid, xy_pos, self.abstand,
            )
            if zi is None:
                continue

            # R-validated version (correct — has parscale, retry, b ≥ 0.01)
            est_validated = pairwise_density_optim_local(
                self.sim_3d, 5.0, 1.0, x, y, self.grid,
                abstand=self.abstand, ensemble=5,
                lower_bounds=(0.01, 0.01),
                upper_bounds=(15.0, 15.0),
            )

            # CMIP6 pipeline version (under test) — with specified backend
            with _force_backend(backend):
                est_backend = optimize_local_mle(
                    zi, zj, xl, yl, 5.0, 1.0, ensemble=5, seed=42,
                )

            if not np.allclose(est_backend, est_validated, atol=0.5):
                mismatches.append({
                    "point": int(row["grid_point"]),
                    "x": x, "y": y,
                    "validated": est_validated.tolist(),
                    "backend": est_backend.tolist(),
                })

        assert len(mismatches) == 0, (
            f"optimize_local_mle [{backend}] differs from "
            f"pairwise_density_optim_local "
            f"at {len(mismatches)}/{len(self.ref_pts)} selected points:\n"
            + "\n".join(
                f"  Point {m['point']} ({m['x']:.2f},{m['y']:.2f}): "
                f"validated=({m['validated'][0]:.3f}, {m['validated'][1]:.3f}, "
                f"{m['validated'][2]:.3f}) vs "
                f"backend=({m['backend'][0]:.3f}, {m['backend'][1]:.3f}, "
                f"{m['backend'][2]:.3f})"
                for m in mismatches
            )
        )


