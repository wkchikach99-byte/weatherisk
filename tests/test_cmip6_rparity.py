def _load_r_simulation(grid):
    """Load R simulation data and reshape to (nrow, ncol, n_sim)."""
    sim_ref = pd.read_csv(REF / "simulation_nonstat_seed42.csv")
    sim_cols = [c for c in sim_ref.columns if c.startswith("sim")]
    n_sim = len(sim_cols)

    sim_3d = np.zeros((grid.nrow, grid.ncol, n_sim))
    for r_idx in range(grid.n_grid):
        r_i = r_idx % grid.nrow   # column-major: row = idx % nrow
        r_j = r_idx // grid.nrow  # col = idx // nrow
        for k, col in enumerate(sim_cols):
            sim_3d[r_i, r_j, k] = sim_ref.iloc[r_idx][col]

    return sim_3d, n_sim


def _build_pair_arrays(sim_3d, grid, xy_pos, abstand):
    """Build (zi, zj, xl, yl) pair arrays for a grid point.

    Replicates the pair construction in pairwise_density_optim_local
    so we can feed the same inputs to optimize_local_mle.
    """
    i0, j0 = grid.number_grid(xy_pos)
    n_sim = sim_3d.shape[2]

    sel_indices = []
    for dx in range(-abstand, abstand + 1):
        for dy in range(-abstand, abstand + 1):
            if 0 < dx * dx + dy * dy <= abstand * abstand:
                ni, nj = i0 + dy, j0 + dx
                if 0 <= ni < grid.nrow and 0 <= nj < grid.ncol:
                    sel_indices.append(grid.grid_number(ni, nj))

    if not sel_indices:
        return None, None, None, None

    sel_indices = np.array(sel_indices)
    ci, cj = grid.number_grid(xy_pos)
    z_centre = sim_3d[ci, cj, :]

    sel_rows = np.array([grid.number_grid(idx) for idx in sel_indices])
    z_neighbours = sim_3d[sel_rows[:, 0], sel_rows[:, 1], :]

    zi = z_neighbours.reshape(-1)
    zj = np.tile(z_centre, len(sel_indices))
    xl = np.repeat(grid.X_flat[sel_indices] - grid.X_flat[xy_pos], n_sim)
    yl = np.repeat(grid.Y_flat[sel_indices] - grid.Y_flat[xy_pos], n_sim)

    return zi, zj, xl, yl


